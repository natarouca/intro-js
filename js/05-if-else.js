
// Condicional composta (if e else)
    // Na condicional composta, há uma estrutura complementar, ou sej, um bloco de código com instruções para lidar com a negação do valor lógico de if (falso).
    
    let idade = 18;
    if (idade >= 18) {
        
        // Se a variável idade atender a condição estabelecida, este bloco de código é executado.
        console.log("Você é maior de idade.");
        alert("Idade permitida!")
        // Valor lógico: true
    } else {
        
        // Caso a condição não for atendida, este bloco de código é executado.
        console.log("Você é menor de idade");
        alert("Idade não permitida!");
        // Valor lógico: false. 
    }
    
